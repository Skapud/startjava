package com.startjava.lesson_1.base;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");
        // цифра
        int digit = 1;
        System.out.println("Цифра = " + digit);

        // сумма чисел
        int sumNums = 10;
        System.out.println("Сумма чисел = " + sumNums);

        // произведение цифр
        int productDigits = 15;
        System.out.println("Произведение цифр = " + productDigits);

        // максимальное число
        int maxNum = 20;
        System.out.println("Максимальное число = " + maxNum);

        // количество десятков
        int numOfTens = 25;
        System.out.println("Количество десятков = " + numOfTens);

        // вес собаки
        int dogWeight = 30;
        System.out.println("Вес собаки = " + dogWeight);

        // исходное число
        int sourceNum = 35;
        System.out.println("Исходное число = " + sourceNum);

        // процент по вкладу
        double rate = 11.1;
        System.out.println("Процент по вкладу = " + rate);

        // переменная хранит символ &
        String symbol = "&";
        System.out.println("Переменная хранит символ & = " + symbol);

        // код ошибки
        int errorCode = 12;
        System.out.println("Код ошибки = " + errorCode);

        // тип сообщения
        String messageType = "Cursive";
        System.out.println("Тип сообщения = " + messageType);

        // число нулей
        int zeroNums = 6;
        System.out.println("Число нулей = " + zeroNums);

        // уникальное число
        double uniqueNum = 666.66;
        System.out.println("Уникальное число = " + uniqueNum);

        // случайное число
        double randomNum = 10.15;
        System.out.println("Случайное число = " + randomNum);

        // математическое выражение
        String mathExpression = "(15 + 15) * 15";
        System.out.println("Математическое выражение = " + mathExpression);

        // счет в игре
        String gameScore = "1 - 2";
        System.out.println("Cчет в игре = " + gameScore);

        // максимальная длина
        int maxLength = 100;
        System.out.println("Максимальная длина = " + maxLength);

        // пункт меню
        String menuPoint = "audio";
        System.out.println("Пункт меню = " + menuPoint);

        // стоимость кофе
        double coffeePrice = 33.5;
        System.out.println("Cтоимость кофе = " + coffeePrice);

        // дата начала
        String beginDate = "11.06.2024";
        System.out.println("Дата начала = " + beginDate);

        // окончание диапазона
        int endRange = 5000;
        System.out.println("Окончание диапазона = " + endRange);

        // полное имя работника месяца
        String employeeMonthFullName = "Бари Алибасов";
        System.out.println("Полное имя работника месяца = " + employeeMonthFullName);

        // заголовок электронной книги
        String ebookTitle = "Человек-невидимка";
        System.out.println("Заголовок электронной книги = " + ebookTitle);

        // размер
        String size = "XL";
        System.out.println("Размер = " + size);

        // вместимость
        int capacity = 111;
        System.out.println("Вместимость = " + capacity);

        // счетчик
        int count = 333;
        System.out.println("Счетчик = " + count);

        // путь до файла
        String filePath = "/user/documents/java/lesson1";
        System.out.println("Путь до файла = " + filePath);

        // количество чисел в каждой строке
        int numOfDigitsInEachLine = 1000;
        System.out.println("Количество чисел в каждой строке = " + numOfDigitsInEachLine);

        System.out.println("\n" + "2. Boolean-переменные");

        // сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Cотни равны? " + isEqualHundreds);

        // компьютер включен?
        boolean isOnPc = false;
        System.out.println("Компьютер включен? " + isOnPc);

        // есть равные цифры?
        boolean hasEqualDigits = true;
        System.out.println("Есть равные цифры? " + hasEqualDigits);

        // создано?
        boolean isCreated = true;
        System.out.println("Создано? " + isCreated);

        // пустое?
        boolean empty = false;
        System.out.println("Пустое? " + empty);

        // активное?
        boolean active = true;
        System.out.println("Активное? " + active);

        // новое?
        boolean isNew = false;
        System.out.println("Новое? " + isNew);

        // электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? " + isValidEmail);

        // имеются уникальные строки?
        boolean hasUniqueLines = true;
        System.out.println("Имеются уникальные строки? " + hasUniqueLines);

        System.out.println("\n" + "3. Аббревиатуры"); 

        // старый universally unique identifier
        String oldUuid = "123e4567-e89b-12d3-a456-426614174000";
        System.out.println("Old UUID = " + oldUuid);

        // производитель оперативной памяти
        String ramManufacturer = "Corsair";
        System.out.println("RAM Manufacturer = " + ramManufacturer);

        // емкость жесткого диска
        String hdCapacity = "500 mb";
        System.out.println("HD Capacity = " + hdCapacity);

        // протокол передачи гипертекста
        String http = "GET /wiki/страница HTTP/1.1";
        System.out.println("HTTP = " + http);

        // сокращенный uniform resource locator
        String shortUrl = "ya.ru";
        System.out.println("Short URL = " + shortUrl);

        // новый идентификатор клиента
        String newCid = "watermellon";
        System.out.println("New CID = " + newCid);

        // american standard code for information interchange
        int ascii = 322;
        System.out.println("ASCII = " + ascii);
    }
}
